export const PALETTES = {
    SAM3KAO,
    SAM4KAO,
    HORIZON_KAO,
    HERO,
    RPGMK
};

export function generatePaletteFile(name: string, outputPath: string): void {
    const palette = PALETTES[name as keyof typeof PALETTES];
    if (!palette) {
        throw new Error(`Unknown palette: ${name}`);
    }
    fs.writeFileSync(outputPath, palette);
}

export function generateAllPalettes(outputDir: string): void {
    if (!fs.existsSync(outputDir)) {
        fs.mkdirSync(outputDir, { recursive: true });
    }

    generatePaletteFile('SAM3KAO', path.join(outputDir, 'SAM3KAO.PAL'));
    generatePaletteFile('SAM4KAO', path.join(outputDir, 'SAM4KAO.PAL'));
    generatePaletteFile('HORIZON_KAO', path.join(outputDir, 'HORIZON_KAO.PAL'));
    generatePaletteFile('HERO', path.join(outputDir, 'HERO.PAL'));
    generatePaletteFile('RPGMK', path.join(outputDir, 'RPGMK.PAL'));
}

// CLI usage
if (require.main === module) {
    const outputDir = process.argv[2] || '.';
    generateAllPalettes(outputDir);
    console.log(`Palette files generated in: ${outputDir}`);
}
